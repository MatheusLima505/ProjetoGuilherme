    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        CustomerDAO dao = null;
        try {
            dao = new CustomerDAO();
        } catch (SQLException ex) {
            Logger.getLogger(Adicionar.class.getName()).log(Level.SEVERE, null, ex);
        }
        int sid=0;
        if (storeid1.isSelected()) {
            sid=1;
            
        }
        if (storeid2.isSelected()) {
            sid=2;
        }
        int act=0;
        if (active.isSelected()) {
            act=1;
            
        } else {
            act=0;
        }
        

        if (sid==0||nome.getText().trim().isEmpty()||sobrenome.getText().trim().isEmpty()||email.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "chaves");
        } else {
            String name = nome.getText();
            String sname = sobrenome.getText();
            String e = email.getText();
            int add = address.getValue();
            Customer c = new Customer(sid, name, sname, e, add, act);
            try {
                dao.insertCustomer(c);
            } catch (SQLException ex) {
                Logger.getLogger(Adicionar.class.getName()).log(Level.SEVERE, null, ex);
            }
                finally {
                JOptionPane.showMessageDialog(rootPane, "Usu√°rio inserido");
            }
        }

        

    }//GEN-LAST:event_jButton1ActionPerformed

